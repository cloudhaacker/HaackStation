#pragma once
// game_scanner.h
// Scans directories for PS1 disc images and builds the game library.
//
// AUTO M3U GENERATION:
//   If a folder contains multiple disc files for the same game
//   (e.g. "Final Fantasy VII (Disc 1).chd", "Final Fantasy VII (Disc 2).chd")
//   HaackStation automatically creates a "Final Fantasy VII.m3u" file.
//   The user never needs to know what an M3U file is.
//   Recognises patterns: (Disc N), (Disk N), (CD N), Disc N, CD N
//
// M3U DE-DUPLICATION:
//   Once M3U files exist (auto or manual), disc files they reference are
//   suppressed. The shelf shows one clean entry per multi-disc game.
//
// ALPHABETICAL SORTING:
//   Games are sorted alphabetically, ignoring leading articles
//   ("The", "A", "An") so "The Legend of Dragoon" sorts under L.

#include "disc_formats.h"
#include <string>
#include <vector>
#include <unordered_set>
#include <functional>

struct GameEntry {
    std::string   path;
    std::string   title;
    DiscFormat    format;
    bool          isMultiDisc = false;
    int           discCount   = 1;
    std::string   region;
    std::string   serial;
    std::string   coverArtPath;
};

struct ScanResult {
    std::vector<GameEntry> games;
    int totalScanned   = 0;
    int suppressed     = 0;
    int invalidSkipped = 0;
    int m3uGenerated   = 0;  // Auto-generated M3U files this scan
};

class GameScanner {
public:
    GameScanner();

    void addSearchPath(const std::string& dir);

    ScanResult scanAll();
    ScanResult scanDirectory(const std::string& dir);
    ScanResult rescan();

    const std::vector<GameEntry>& getLibrary() const { return m_library; }

    using ProgressCallback = std::function<void(int, int, const std::string&)>;
    void setProgressCallback(ProgressCallback cb) { m_progress = cb; }

    static std::vector<std::string> defaultPaths();
    void scanDefaultPaths();

    // Returns paths of any M3U files auto-generated in the last scan
    const std::vector<std::string>& autoGeneratedM3us() const {
        return m_autoGeneratedM3us;
    }

private:
    ScanResult scanDirectoryInternal(const std::string& dir);

    // Auto-generate M3U files for multi-disc games in a directory
    // Returns number of M3U files created
    int autoGenerateM3u(const std::string& dirPath);

    std::unordered_set<std::string> buildSuppressedSet(
        const std::string& dir,
        std::vector<GameEntry>& m3uEntries
    );

    static std::string normalizePath(const std::string& path);

    std::vector<std::string>   m_searchPaths;
    std::vector<GameEntry>     m_library;
    std::vector<std::string>   m_autoGeneratedM3us;
    ProgressCallback           m_progress;
};
