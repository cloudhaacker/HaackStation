// ─── Sort key: strip leading articles for alphabetical sorting ────────────────
static std::string sortKey(const std::string& title) {
    std::string t = title;
    // Convert to lowercase for comparison
    std::string lower = t;
    std::transform(lower.begin(), lower.end(), lower.begin(), ::tolower);

    // Strip leading articles
    const char* articles[] = { "the ", "a ", "an " };
    for (auto* art : articles) {
        if (lower.substr(0, strlen(art)) == art) {
            t = t.substr(strlen(art));
            break;
        }
    }
    // Lowercase for final comparison
    std::transform(t.begin(), t.end(), t.begin(), ::tolower);
    return t;
}

// ─── Disc indicator stripping ─────────────────────────────────────────────────
// Returns the base game name with disc indicators removed
// "Final Fantasy VII (USA) (Disc 2)" -> "Final Fantasy VII (USA)"
static std::string stripDiscIndicator(const std::string& stem) {
    // Patterns to strip (case-insensitive): (Disc N), (Disk N), (CD N), Disc N, CD N
    static const std::vector<std::string> patterns = {
        " (Disc ",  " (Disk ",  " (CD ",   " (cd ",
        " (disc ",  " (disk ",  " Disc ",   " Disk ",
        " CD ",     " cd ",
    };

    std::string result = stem;
    for (const auto& pat : patterns) {
        auto pos = result.find(pat);
        if (pos != std::string::npos) {
            // Everything from the disc indicator onwards is removed
            result = result.substr(0, pos);
            break;
        }
    }

    // Remove trailing whitespace
    while (!result.empty() && result.back() == ' ')
        result.pop_back();

    return result;
}

// ─── Auto-generate M3U files for multi-disc games ────────────────────────────
// Scans directory for groups of disc files that share a base name.
// Creates .m3u files automatically so users don't have to.
// Returns number of M3U files created.
int GameScanner::autoGenerateM3u(const std::string& dirPath) {
    if (!fs::exists(dirPath) || !fs::is_directory(dirPath))
        return 0;

    // Group disc files by their base name
    // Key: base name (with region, without disc indicator)
    // Value: sorted list of full paths
    std::map<std::string, std::vector<std::string>> discGroups;

    for (const auto& entry : fs::directory_iterator(dirPath)) {
        if (!entry.is_regular_file()) continue;
        std::string ext = entry.path().extension().string();
        if (!DiscFormats::isDiscExtension(ext)) continue;
        if (DiscFormats::detectFormat(entry.path().string()) == DiscFormat::M3U)
            continue;

        std::string stem = entry.path().stem().string();
        std::string base = stripDiscIndicator(stem);

        // Only group if the name actually changed (i.e. had a disc indicator)
        if (base != stem) {
            discGroups[base].push_back(entry.path().string());
        }
    }

    int created = 0;
    for (auto& [baseName, discs] : discGroups) {
        if (discs.size() < 2) continue; // Need at least 2 discs

        // Sort discs in order (Disc 1, Disc 2, etc.)
        std::sort(discs.begin(), discs.end());

        // Build the M3U path
        std::string m3uPath = dirPath;
        if (m3uPath.back() != '/' && m3uPath.back() != '\\')
            m3uPath += '/';
        m3uPath += baseName + ".m3u";

        // Don't overwrite existing M3U
        if (fs::exists(m3uPath)) continue;

        // Write the M3U file
        std::ofstream m3u(m3uPath);
        if (!m3u.is_open()) {
            std::cerr << "[GameScanner] Cannot write M3U: " << m3uPath << "\n";
            continue;
        }

        m3u << "# Auto-generated by HaackStation\n";
        for (const auto& disc : discs) {
            // Write just the filename, not the full path
            m3u << fs::path(disc).filename().string() << "\n";
        }
        m3u.close();

        std::cout << "[GameScanner] Auto-generated M3U: " << m3uPath
                  << " (" << discs.size() << " discs)\n";
        created++;
        m_autoGeneratedM3us.push_back(m3uPath);
    }

    return created;
}

// ─── Search path management ───────────────────────────────────────────────────
void GameScanner::addSearchPath(const std::string& dir) {
    // Avoid duplicates
    for (const auto& p : m_searchPaths)
        if (p == dir) return;
    m_searchPaths.push_back(dir);
    std::cout << "[GameScanner] Added search path: " << dir << "\n";
}

void GameScanner::scanDefaultPaths() {
    auto defaults = defaultPaths();
    for (const auto& p : defaults) {
        if (fs::exists(p)) {
            addSearchPath(p);
        }
    }
    if (!m_searchPaths.empty()) {
        scanAll();
    }
}

// ─── Main scan ────────────────────────────────────────────────────────────────
ScanResult GameScanner::scanAll() {
    ScanResult total;
    m_library.clear();
    m_autoGeneratedM3us.clear();

    for (const auto& dir : m_searchPaths) {
        // Auto-generate M3U files before scanning
        int generated = autoGenerateM3u(dir);
        if (generated > 0) {
            std::cout << "[GameScanner] Created " << generated
                      << " M3U file(s) in " << dir << "\n";
            total.m3uGenerated += generated;
        }

        ScanResult r = scanDirectoryInternal(dir);
        for (auto& g : r.games) m_library.push_back(std::move(g));
        total.totalScanned   += r.totalScanned;
        total.suppressed     += r.suppressed;
        total.invalidSkipped += r.invalidSkipped;
    }

    // Sort library alphabetically, ignoring leading articles
    std::sort(m_library.begin(), m_library.end(),
        [](const GameEntry& a, const GameEntry& b) {
            return sortKey(a.title) < sortKey(b.title);
        });

    std::cout << "[GameScanner] Library built: " << m_library.size()
              << " games, " << total.suppressed
              << " disc files suppressed by M3U, "
              << total.invalidSkipped << " invalid files skipped";
    if (total.m3uGenerated > 0)
        std::cout << ", " << total.m3uGenerated << " M3U files auto-generated";
    std::cout << "\n";

    return total;
}

ScanResult GameScanner::rescan() {
    return scanAll();
}

ScanResult GameScanner::scanDirectory(const std::string& dir) {
    addSearchPath(dir);
    return scanAll();
}

// ─── Cover art helper ─────────────────────────────────────────────────────────
static std::string findCoverArt(const std::string& displayName,
                                 const std::string& romPath) {
    auto tryPath = [](const std::string& p) {
        return fs::exists(p) ? p : "";
    };

    // 1. Clean display title
    for (auto ext : {".png", ".jpg"}) {
        std::string p = tryPath("media/covers/" + displayName + ext);
        if (!p.empty()) return p;
    }

    // 2. Original ROM filename stem
    std::string stem = fs::path(romPath).stem().string();
    if (stem != displayName) {
        for (auto ext : {".png", ".jpg"}) {
            std::string p = tryPath("media/covers/" + stem + ext);
            if (!p.empty()) return p;
        }
    }

    return "";
}

// ─── Scan a single directory ──────────────────────────────────────────────────
ScanResult GameScanner::scanDirectoryInternal(const std::string& dirPath) {
    ScanResult result;

    if (!fs::exists(dirPath) || !fs::is_directory(dirPath)) {
        std::cerr << "[GameScanner] Not a directory: " << dirPath << "\n";
        return result;
    }

    // Pass 1: M3U files (includes auto-generated ones)
    std::vector<GameEntry> m3uEntries;
    auto suppressed = buildSuppressedSet(dirPath, m3uEntries);

    for (auto& entry : m3uEntries) {
        result.games.push_back(std::move(entry));
        result.totalScanned++;
    }

    // Pass 2: Individual disc files (skip suppressed)
    for (const auto& dirEntry : fs::directory_iterator(dirPath)) {
        if (!dirEntry.is_regular_file()) continue;

        std::string path = dirEntry.path().string();
        std::string ext  = dirEntry.path().extension().string();

        if (DiscFormats::detectFormat(path) == DiscFormat::M3U) continue;
        if (!DiscFormats::isDiscExtension(ext)) continue;

        result.totalScanned++;

        if (suppressed.count(normalizePath(path))) {
            result.suppressed++;
            std::cout << "[GameScanner] Suppressed (covered by M3U): "
                      << dirEntry.path().filename().string() << "\n";
            continue;
        }

        DiscInfo info = DiscFormats::validate(path);
        if (!info.valid) {
            result.invalidSkipped++;
            std::cerr << "[GameScanner] Skipping: "
                      << dirEntry.path().filename().string()
                      << " — " << info.errorReason << "\n";
            continue;
        }

        GameEntry entry;
        entry.path         = info.path;
        entry.title        = info.displayName;
        entry.format       = info.format;
        entry.coverArtPath = findCoverArt(info.displayName, info.path);

        result.games.push_back(std::move(entry));
    }

    return result;
}

// ─── Build suppressed set ─────────────────────────────────────────────────────
std::unordered_set<std::string> GameScanner::buildSuppressedSet(
    const std::string& dirPath,
    std::vector<GameEntry>& m3uEntries)
{
    std::unordered_set<std::string> suppressed;

    for (const auto& entry : fs::directory_iterator(dirPath)) {
        if (!entry.is_regular_file()) continue;
        if (DiscFormats::detectFormat(entry.path().string()) != DiscFormat::M3U)
            continue;

        std::string m3uPath = entry.path().string();
        DiscInfo info = DiscFormats::validate(m3uPath);
        if (!info.valid) continue;

        // Suppress all disc files referenced by this M3U
        for (const auto& disc : info.m3uDiscs) {
            suppressed.insert(normalizePath(disc));
        }

        // Create library entry for the M3U
        GameEntry m3uEntry;
        m3uEntry.path        = info.path;
        m3uEntry.title       = info.displayName;
        m3uEntry.format      = DiscFormat::M3U;
        m3uEntry.isMultiDisc = true;
        m3uEntry.discCount   = static_cast<int>(info.m3uDiscs.size());
        m3uEntry.coverArtPath = findCoverArt(info.displayName, info.path);

        std::cout << "[GameScanner] Multi-disc game: \""
                  << m3uEntry.title << "\" ("
                  << m3uEntry.discCount << " discs)\n";

        m3uEntries.push_back(std::move(m3uEntry));
    }

    return suppressed;
}

// ─── Path normalization ───────────────────────────────────────────────────────
std::string GameScanner::normalizePath(const std::string& path) {
    std::string norm;
    try {
        norm = fs::canonical(path).string();
    } catch (...) {
        norm = fs::absolute(path).string();
    }
    std::transform(norm.begin(), norm.end(), norm.begin(), ::tolower);
    for (auto& c : norm) if (c == '\\') c = '/';
    return norm;
}

// ─── Default paths ────────────────────────────────────────────────────────────
std::vector<std::string> GameScanner::defaultPaths() {
    std::vector<std::string> paths;
#if defined(_WIN32)
    if (const char* appdata = std::getenv("APPDATA"))
        paths.push_back(std::string(appdata) + "\\HaackStation\\roms");
    paths.push_back("C:\\Emulation\\roms\\psx");
    paths.push_back("C:\\Emulation\\roms\\ps1");
    paths.push_back("C:\\Emulation\\PS1");
    paths.push_back("C:\\Games\\PS1");
    paths.push_back("C:\\ROMs\\PS1");
    paths.push_back("C:\\ROMs\\PlayStation");
    for (char drive = 'D'; drive <= 'F'; drive++) {
        paths.push_back(std::string(1, drive) + ":\\Emulation\\roms\\psx");
        paths.push_back(std::string(1, drive) + ":\\ROMs\\PS1");
    }
#elif defined(__ANDROID__)
    paths.push_back("/sdcard/ROMs/PS1");
    paths.push_back("/sdcard/ROMs/PlayStation");
    paths.push_back("/sdcard/HaackStation/roms");
    paths.push_back("/storage/emulated/0/ROMs/PS1");
#else
    if (const char* home = std::getenv("HOME")) {
        paths.push_back(std::string(home) + "/.config/haackstation/roms");
        paths.push_back(std::string(home) + "/ROMs/PS1");
        paths.push_back(std::string(home) + "/Games/PS1");
    }
#endif
    return paths;
}
